def _build_tasks():
    tasks = []

    for text in TRUTH_REGULAR:
        tasks.append(("truth", text, "none", False))

    for item in DARE_REGULAR:
        if isinstance(item, tuple):
            text, media = item
        else:
            text, media = item, "none"
        tasks.append(("dare", text, media, False))

    for text in TRUTH_18:
        tasks.append(("truth", text, "none", True))

    for item in DARE_18:
        if isinstance(item, tuple):
            text, media = item
        else:
            text, media = item, "none"
        tasks.append(("dare", text, media, True))

    return tasks


async def seed():
    from app.config import settings
    engine = create_async_engine(settings.database_url)
    sm = async_sessionmaker(bind=engine, class_=AsyncSession, expire_on_commit=False)

    tasks = _build_tasks()

    async with sm() as session:
        # Проверяем что не дублируем
        existing_result = await session.execute(select(func.count(TasksPool.id)))
        existing_count = existing_result.scalar() or 0

        if existing_count >= len(tasks):
            print(f"✅ Пул уже содержит {existing_count} заданий — пропускаем")
            await engine.dispose()
            return

        # Добавляем только отсутствующие
        existing_texts_result = await session.execute(select(TasksPool.text))
        existing_texts = {row[0] for row in existing_texts_result.all()}

        added = 0
        for task_type, text, media_req, is_18 in tasks:
            if text in existing_texts:
                continue
            session.add(TasksPool(
                type=TaskType(task_type),
                is_18_plus=is_18,
                text=text,
                media_required=MediaRequired(media_req),
            ))
            added += 1

        await session.commit()

    await engine.dispose()

    regular = sum(1 for _, _, _, is18 in tasks if not is18)
    plus18 = sum(1 for _, _, _, is18 in tasks if is18)
    print(f"✅ Добавлено {added} заданий (всего в пуле: {regular} обычных + {plus18} для 18+)")


if __name__ == "__main__":
    import asyncio
    asyncio.run(seed())
